    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(sex != null && father != null && !firstnameText.getText().isEmpty() && !surnameText.getText().isEmpty() && !charClass.isEmpty()){
                exitButton = "OK";
                switch (charClass) {
                    case "Mage":
                        character = new Mage(firstnameText.getText(), surnameText.getText(), sex, father);
                        break;
                    case "Warrior":
                        character = new Warrior(firstnameText.getText(), surnameText.getText(), sex, father);
                        break;
                    case "Rogue":
                        character = new Rogue(firstnameText.getText(), surnameText.getText(), sex, father);
                        break;
                }
                this.dispose();
            }
    
    }//GEN-LAST:event_jButton1ActionPerformed

