    private void classListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_classListValueChanged
        switch (classList.getSelectedValue()) {
            case "Mage":
                charClass = "Mage";
                classDescription.setText(Mage.getDescription());
                break;
            case "Warrior":
                charClass = "Warrior";
                classDescription.setText(Warrior.getDescription());
                break;
            case "Rogue":
                charClass = "Rogue";
                classDescription.setText(Rogue.getDescription());
                break;
        }
    }//GEN-LAST:event_classListValueChanged

    private void fatherComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fatherComboActionPerformed
        switch((String) fatherCombo.getSelectedItem()){
            case "merchant":
                father=Father.MERCHANT;
                break;
            case "knight":
                father=Father.KNIGHT;
                break;
            case "mercenary":
                father=Father.MERCENARY;
                break;
            case "blacksmith":
                father=Father.BLACKSMITH;
                break;
        }
    }//GEN-LAST:event_fatherComboActionPerformed

